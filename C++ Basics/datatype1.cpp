#include<iostream>
#include<string>
int main(){
    int myNum = 9;
    double myDoubleNum = 8.99;
    char myLetter = 'A';
    bool myBool = false;
    std::string myText = "Hello World";

    std::cout << "myNum = " << myNum << std::endl;
    std::cout << "myDoubleNum = " << myDoubleNum << std::endl;
    std::cout << "myLetter = " << myLetter << std::endl;
    std::cout << "myBool = " << myBool << std::endl;
    std::cout << "myText = " << myText << std::endl;

    return 0;
}
